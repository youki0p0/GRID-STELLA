/* ---------------------------------------------------------------- 関数 */

/**
 * wave 番号から決定的にボス台詞を選択する。
 * 同じ wave 番号には常に同じ台詞が対応する（乱数不使用）。
 * @param wave 波番号（0以上の整数）
 * @returns BOSS_LINES の要素の一つ
 */
export function bossLine(wave: number): string {
  return BOSS_LINES[Math.abs(Math.floor(wave)) % BOSS_LINES.length];
}

