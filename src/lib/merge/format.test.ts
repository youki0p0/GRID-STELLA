import { describe, it, expect } from 'vitest';
import { abbrev, signedPct, pct, clampInt } from './format';

/* ------------------------------------------------------------------ abbrev */
describe('abbrev', () => {
  it('0 → "0"', () => {
    expect(abbrev(0)).toBe('0');
  });

  it('999 → "999"', () => {
    expect(abbrev(999)).toBe('999');
  });

  it('1000 → "1.0K"', () => {
    expect(abbrev(1000)).toBe('1.0K');
  });

  it('1234 → "1.2K"', () => {
    expect(abbrev(1234)).toBe('1.2K');
  });

  it('1250 → "1.3K" (四捨五入)', () => {
    expect(abbrev(1250)).toBe('1.3K');
  });

  it('999999 → "1000.0K" ではなく "1.0M" に最も近いKを返す', () => {
    // 999_999 → Math.round(999_999/100)/10 = Math.round(9999.99)/10 = 10000/10 = 1000.0 → '1000.0K'
    // 実装上 <1_000_000 なので K になる
    expect(abbrev(999_999)).toBe('1000.0K');
  });

  it('1_500_000 → "1.5M"', () => {
    expect(abbrev(1_500_000)).toBe('1.5M');
  });

  it('3_400_000 → "3.4M"', () => {
    expect(abbrev(3_400_000)).toBe('3.4M');
  });

  it('1_000_000_000 → "1.0B"', () => {
    expect(abbrev(1_000_000_000)).toBe('1.0B');
  });

  it('負数: -500 → "-500"', () => {
    expect(abbrev(-500)).toBe('-500');
  });

  it('負数: -1234 → "-1.2K"', () => {
    expect(abbrev(-1234)).toBe('-1.2K');
  });

  it('負数: -2_500_000 → "-2.5M"', () => {
    expect(abbrev(-2_500_000)).toBe('-2.5M');
  });

  it('負数: -1_000_000_000 → "-1.0B"', () => {
    expect(abbrev(-1_000_000_000)).toBe('-1.0B');
  });
});

/* ------------------------------------------------------------------ signedPct */
describe('signedPct', () => {
  it('1 → "+0%"', () => {
    expect(signedPct(1)).toBe('+0%');
  });

  it('1.25 → "+25%"', () => {
    expect(signedPct(1.25)).toBe('+25%');
  });

  it('0.8 → "-20%"', () => {
    expect(signedPct(0.8)).toBe('-20%');
  });

  it('2.0 → "+100%"', () => {
    expect(signedPct(2.0)).toBe('+100%');
  });

  it('0.0 → "-100%"', () => {
    expect(signedPct(0.0)).toBe('-100%');
  });

  it('1.005 → 浮動小数点精度により "+0%" (1.005-1)*100 = 0.4999...)', () => {
    // JS では (1.005 - 1) * 100 = 0.49999... なので Math.round → 0
    expect(signedPct(1.005)).toBe('+0%');
  });

  it('1.1 → "+10%"', () => {
    expect(signedPct(1.1)).toBe('+10%');
  });
});

/* ------------------------------------------------------------------ pct */
describe('pct', () => {
  it('0 → "0%"', () => {
    expect(pct(0)).toBe('0%');
  });

  it('1 → "100%"', () => {
    expect(pct(1)).toBe('100%');
  });

  it('0.5 → "50%"', () => {
    expect(pct(0.5)).toBe('50%');
  });

  it('0.333 → "33%"（整数丸め）', () => {
    expect(pct(0.333)).toBe('33%');
  });

  it('0.666 → "67%"（整数丸め）', () => {
    expect(pct(0.666)).toBe('67%');
  });

  it('負数はクランプ: -0.5 → "0%"', () => {
    expect(pct(-0.5)).toBe('0%');
  });

  it('1超はクランプ: 1.5 → "100%"', () => {
    expect(pct(1.5)).toBe('100%');
  });

  it('大きな負数はクランプ: -100 → "0%"', () => {
    expect(pct(-100)).toBe('0%');
  });

  it('大きな正数はクランプ: 999 → "100%"', () => {
    expect(pct(999)).toBe('100%');
  });
});

/* ------------------------------------------------------------------ clampInt */
describe('clampInt', () => {
  it('floor: 3.9 → 3 in [0,5]', () => {
    expect(clampInt(3.9, 0, 5)).toBe(3);
  });

  it('floor: 3.1 → 3 in [0,5]', () => {
    expect(clampInt(3.1, 0, 5)).toBe(3);
  });

  it('下限クランプ: -1 → 0 in [0,5]', () => {
    expect(clampInt(-1, 0, 5)).toBe(0);
  });

  it('上限クランプ: 10 → 5 in [0,5]', () => {
    expect(clampInt(10, 0, 5)).toBe(5);
  });

  it('境界値: ちょうど lo → lo', () => {
    expect(clampInt(0, 0, 5)).toBe(0);
  });

  it('境界値: ちょうど hi → hi', () => {
    expect(clampInt(5, 0, 5)).toBe(5);
  });

  it('負のlo/hi: clampInt(-1.7, -5, -1) → -2', () => {
    expect(clampInt(-1.7, -5, -1)).toBe(-2);
  });

  it('floor の結果が hi を超えない: 5.9 in [0,5] → 5', () => {
    expect(clampInt(5.9, 0, 5)).toBe(5);
  });
});
