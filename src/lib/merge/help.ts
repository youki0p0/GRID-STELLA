/* ---------------------------------------------------------------- 関数 */

/**
 * index を安全にラップして TIPS の要素を返す。
 * 負数インデックスや配列長を超えるインデックスにも対応する。
 * @param index 任意の整数（負数可）
 * @returns TIPS の要素の一つ
 */
export function tipAt(index: number): string {
  const len = TIPS.length;
  return TIPS[((index % len) + len) % len];
}

/**
 * rng を使ってランダムにヒントを一件返す。
 * rng のデフォルトは Math.random（テスト時は差し替え可能）。
 * @param rng 0以上1未満の乱数を返す関数（省略時は Math.random）
 * @returns TIPS の要素の一つ
 */
export function randomTip(rng: () => number = Math.random): string {
  return TIPS[Math.floor(rng() * TIPS.length)];
}
